# Integer Comparison
user_choice1 = int(input("Enter the first integer: "))
user_choice2 = int(input("Enter the second integer: "))

if user_choice1 == user_choice2:
    print("These numbers are equal.")
if user_choice1 > user_choice2:
    print(user_choice1,"is larger.")
if user_choice2 > user_choice1:
    print(user_choice2,"is larger.")
